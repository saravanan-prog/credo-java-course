package javaOperators;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int firstNumber = 100;
        int secondNumber = 500;
        double additionResult = firstNumber + secondNumber;
        double subractResult = firstNumber - secondNumber;
        double multiplicationResult = firstNumber * secondNumber;

        System.out.println("additionResult=====>" + additionResult);
        System.out.println("subractResult=====>" + subractResult);
        System.out.println("multiplicationResult=====>" + multiplicationResult);




    }
}
