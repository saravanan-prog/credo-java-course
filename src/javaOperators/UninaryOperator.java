package javaOperators;

public class UninaryOperator {
    public static void main(String[] args) {
        int a = 100;
        System.out.println("a=====>" + a);
        System.out.println("post increment===>" + a++);
        System.out.println("a=====>" + a);
        System.out.println("Pre increment===>" + ++a);







    }
}
