package javaVariables;

public class StaticVariable {
    static int firstNumber = 100;
    static int secondNumber = 500;

    static double price = 200.25;
    static double tax = 50.25;

    public static void main(String[] args) {

        System.out.println("firstNumber===>" + firstNumber + " " +"secondNumber ====>" + secondNumber);
        System.out.println("price====>" + price +" " + "tax=====>" + tax);

    }
}
