package JavaConditons;

public class Switchstatement {
    public static void main(String[] args) {
        String productItem = "apple";

        switch (productItem){

            case "apple"  -> System.out.println("Apple  is red and green color");
            case "orange" -> System.out.println("orange is light yellow color");
            case "grapes" -> System.out.println("black and green color");
            default -> System.out.println("Generally fruits sweet");
        }




    }
}
